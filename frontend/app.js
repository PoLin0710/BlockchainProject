// 上傳文件到 IPFS
async function uploadToIPFS(file) {
    try {
        const added = await ipfs.add(file);
        return added.path;
    } catch (error) {
        console.error('上傳到IPFS失敗:', error);
    }
}

// 創建元數據
function createMetadata(imageHash, name, description) {
    return JSON.stringify({
        name: name,
        description: description,
        image: `ipfs://${imageHash}`
    });
}

// 鑄造 NFT
async function mintNFT(signer, contractAddress, contractABI) {
    const fileInput = document.getElementById('fileInput');
    if (!fileInput.files.length) {
        console.log("請選擇一個文件");
        return;
    }

    const file = fileInput.files[0];
    const imageHash = await uploadToIPFS(file);
    const metadata = createMetadata(imageHash, "My NFT", "This is my first NFT");
    const metadataHash = await uploadToIPFS(new Blob([metadata]));

    const contract = new ethers.Contract(contractAddress, contractABI, signer);
    try {
        const transaction = await contract.mintNFT(await signer.getAddress(), `ipfs://${metadataHash}`);
        await transaction.wait();
        console.log("NFT鑄造成功!");
    } catch (error) {
        console.error("鑄造NFT失敗:", error);
    }
}

